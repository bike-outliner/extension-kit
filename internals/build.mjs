import esbuild from 'esbuild'
import process from 'process'
import path from 'path'
import fs from 'fs'

const internalsDir = import.meta.dirname
const outdir = path.join(internalsDir, 'out')

console.log(`Building to directory: ${outdir}\n`)

fs.mkdirSync(outdir, { recursive: true })
fs.copyFileSync(path.join(internalsDir, 'sheet.html'), path.join(outdir, 'sheet.html'))
fs.copyFileSync(path.join(internalsDir, 'inspector.html'), path.join(outdir, 'inspector.html'))

const prod = process.argv[2] === 'production'
const context = await esbuild.context({
  banner: { js: '// GENERATED BY ESBUILD' },
  target: 'esnext',
  entryPoints: [
    path.join(internalsDir, 'common'),
    path.join(internalsDir, 'sheet'),
    path.join(internalsDir, 'inspector'),
  ],
  external: ['bike/dom'],
  nodePaths: [path.join(internalsDir, '..', 'node_modules')],
  format: 'iife',
  logLevel: 'info',
  sourcemap: prod ? 'external' : 'inline',
  outdir: outdir,
  bundle: true,
  minify: prod,
  keepNames: true,
})

if (prod) {
  await context.rebuild()
  process.exit(0)
} else {
  await context.watch()
}
