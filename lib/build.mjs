import externalGlobalPlugin from 'esbuild-plugin-external-global'
import { copyAndValidatePlugin, typecheckPlugin, installExtensionPlugin, runBuildContext } from './plugins.mjs'
import { clean } from './clean.mjs'
import esbuild from 'esbuild'
import process from 'process'
import path from 'path'
import addFormats from 'ajv-formats'
import Ajv from 'ajv'
import fs from 'fs'

const packageRoot = path.resolve(import.meta.dirname, '..')
const projectRoot = process.cwd()

export async function build(mode, { install = false, exit = true, id } = {}) {
  const prod = mode === 'production'

  const ajv = new Ajv({ allErrors: true, validateSchema: false })
  addFormats(ajv)
  const manifestSchema = JSON.parse(
    fs.readFileSync(path.join(packageRoot, 'schemas/manifest.schema.json'), 'utf-8')
  )
  const themeSchema = JSON.parse(
    fs.readFileSync(path.join(packageRoot, 'schemas/theme-schema.json'), 'utf-8')
  )
  const validateManifest = ajv.compile(manifestSchema)
  const validateTheme = ajv.compile(themeSchema)

  const outdir = path.join(projectRoot, 'out/extensions')
  const srcDir = path.join(projectRoot, 'src')

  // Extension filter: specific id or all (**)
  const ext = id ? `${id}.bkext` : '**'

  clean(outdir)
  console.log(`Building ${id || 'all extensions'} to directory: ${outdir}\n`)

  // Filter glob entry points whose directories don't exist to avoid esbuild errors
  const entryGlobs = [
    path.join(projectRoot, 'src/copy-and-validate'),
    path.join(projectRoot, `src/${ext}/app/main.ts`),
    path.join(projectRoot, `src/${ext}/style/main.ts`),
    path.join(projectRoot, `src/${ext}/dom/*.ts`),
    path.join(projectRoot, `src/${ext}/dom/*.tsx`),
    path.join(projectRoot, `src/${ext}/tests/*.test.ts`),
  ]
  const entryPoints = entryGlobs.filter(g => {
    // Always include the copy-and-validate virtual entry
    if (!g.includes('*') && !g.includes('.ts')) return true
    // For wildcard dirs (e.g. **/dom), always include — esbuild handles these
    if (path.dirname(g).includes('*')) return true
    // For globs, check the parent directory exists
    if (g.includes('*')) return fs.existsSync(path.dirname(g))
    // For concrete paths, check the file itself exists
    return fs.existsSync(g)
  })

  const context = await esbuild.context({
    banner: { js: '// GENERATED BY ESBUILD' },
    logOverride: { 'empty-glob': 'silent' },
    entryPoints,
    external: ['bike/app', 'bike/dom', 'bike/style', 'bike/components', 'react', 'react-dom'],
    plugins: [
      externalGlobalPlugin.externalGlobalPlugin({
        react: 'window.React',
        'react-dom': 'window.ReactDOM',
        'react-dom/client': 'window.ReactDOMClient',
        'react/jsx-runtime': 'window.ReactJSXRuntime',
        'bike/components': 'window.BikeComponents',
      }),
      copyAndValidatePlugin(outdir, srcDir, validateManifest, validateTheme, {
        manifestPattern: path.join(projectRoot, `src/${ext}/manifest.json`),
        themePattern: path.join(projectRoot, `src/${ext}/theme/*.bktheme`),
      }),
      typecheckPlugin(packageRoot, projectRoot),
      ...(install ? [installExtensionPlugin(outdir)] : []),
    ],

    format: 'iife',
    globalName: 'extensionExports',
    sourcemap: prod ? 'external' : 'inline',
    treeShaking: true,
    outdir: outdir,
    outbase: path.join(projectRoot, 'src'),
    bundle: true,
    minify: prod,
  })

  await runBuildContext(context, mode, { exit })
}
