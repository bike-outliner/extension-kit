import { runBuildContext } from './plugins.mjs'
import esbuild from 'esbuild'
import path from 'path'
import fs from 'fs'

const packageRoot = path.resolve(import.meta.dirname, '..')

export async function buildInternals(mode) {
  const prod = mode === 'production'
  const internalsDir = path.join(packageRoot, 'internals')
  const outdir = path.join(internalsDir, 'out')

  console.log(`Building internals to directory: ${outdir}\n`)

  fs.mkdirSync(outdir, { recursive: true })
  fs.copyFileSync(path.join(internalsDir, 'sheet.html'), path.join(outdir, 'sheet.html'))
  fs.copyFileSync(path.join(internalsDir, 'inspector.html'), path.join(outdir, 'inspector.html'))

  const context = await esbuild.context({
    banner: { js: '// GENERATED BY ESBUILD' },
    target: 'esnext',
    entryPoints: [
      path.join(internalsDir, 'common'),
      path.join(internalsDir, 'sheet'),
      path.join(internalsDir, 'inspector'),
    ],
    external: ['bike/dom'],
    nodePaths: [path.join(packageRoot, 'node_modules')],
    format: 'iife',
    logLevel: 'info',
    sourcemap: prod ? 'external' : 'inline',
    outdir: outdir,
    bundle: true,
    minify: prod,
    keepNames: true,
  })

  await runBuildContext(context, mode)
}
